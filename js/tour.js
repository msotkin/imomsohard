      li = document.createElement("li");
      a = document.createElement("a");
      button = document.createElement("button");

      button.innerHTML = "TICKETS!";
      a.setAttribute("href", tourDates[i].url);
      a.setAttribute("target", "_blank");
      a.appendChild(button);
      
